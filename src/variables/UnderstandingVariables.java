package variables;

public class UnderstandingVariables {
    public static void main(String[] args) {

        //Variable declaration and assignment (declaring there is an age and assigning the age)
        int age = 45;

        System.out.println(age); //It will print 45

        //Variable Declaration - declaring the variable and allocating the memory - basically like saving a seat.
        String name;

        //Variable Assignment
        name = "Suzanne";
        System.out.println(name); //Suzanne

        //Re-assigning
        name = "Saeed";

        System.out.println(name); //Saeed

        age = 21;

        System.out.println(age);





    }
}

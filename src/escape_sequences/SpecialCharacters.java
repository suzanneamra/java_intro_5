package escape_sequences;

public class SpecialCharacters {
    public static void main(String[] args){
        /*
        apple\\banana
        I like "Sundays" and "Apples"
         */
        System.out.println("apple\\banana");
        System.out.println("apple\\orange");
        System.out.println("I Like \"sundays\" and apples");
        System.out.println("My favorite fruits are \"kiwi\" and \"Sundays\"");

    }
}
